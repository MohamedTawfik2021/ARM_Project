{
    Port_PinType                pinId;
	Port_Type                   portId;
	Port_PinDirectionType       pinDir;
	Port_PinModeType            pinMode;
	uint8                       portMux;
	Port_PinOutputCurrentType   currentStrength;
	Port_PinInternalAttachType  internalAttach;

}Port_ConfigType;



