typedef enum 
{   
    PIN_0,
    PIN_1,
    PIN_2,
    PIN_3,
    PIN_4,
    PIN_5,
    PIN_6,
    PIN_7

}Port_PinType;

typedef enum 
{
   PORT_A,
   PORT_B,
   PORT_C,
   PORT_D,
   PORT_E,
   PORT_F

}Port_Type;

typedef enum
{
	IN,
	OUT

}Port_PinDirectionType;


typedef enum
 {
   UART,
   ADC ,
   SSI ,
   I2C ,
   CAN ,
   USB ,
   PWM

 }Port_PinModeType;


